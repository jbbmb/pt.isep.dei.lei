    private void submitLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitLabelMouseClicked
        String[] answers = {"No", "Yes"};
            int confirmDialog = JOptionPane.showOptionDialog(null, "Are you sure you want to submit this?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, QUESTION_ICON, answers, answers[0]);
            if (confirmDialog == 1) {
                boolean status = false;
                if(equallyRadioButton.isSelected() == true){
                    try {
                        status = assignStaffController.applyDistribution(1);
                    } catch (IOException ex) {
                        Logger.getLogger(AssignStaffPanel.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else if(experienceRadioButton.isSelected() == true){
                    try {
                        status = assignStaffController.applyDistribution(2);
                    } catch (IOException ex) {
                        Logger.getLogger(AssignStaffPanel.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else if(minimumRadioButton.isSelected() == true){
                    try {
                        status = assignStaffController.applyDistribution(3);
                    } catch (IOException ex) {
                        Logger.getLogger(AssignStaffPanel.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            if(status == true){
                JOptionPane.showMessageDialog(null, "Assignment and attribution completed with no errors.", "Notification", JOptionPane.ERROR_MESSAGE, CHECK_ICON);
                String result = null;
                if(equallyRadioButton.isSelected() == true){
                    result = assignStaffController.distributionResult(1);
                } else if(experienceRadioButton.isSelected() == true){
                    result = assignStaffController.distributionResult(2);
                } else if(minimumRadioButton.isSelected() == true){
                    result = assignStaffController.distributionResult(3);
                }
                distributionTextArea.setText(result);
            } else if(status == false){
                JOptionPane.showMessageDialog(null, "Impossible to assign via the selected method!\nTry again with another option.", "Error", JOptionPane.ERROR_MESSAGE, ERROR_ICON);
            }
        }
    }//GEN-LAST:event_submitLabelMouseClicked

