    private void botaoContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoContinuarActionPerformed
        String[] respostas = {"Sim", "Não"};
            final ImageIcon icon = new ImageIcon("src/main/resources/question.png");
            int confirmDialog = JOptionPane.showOptionDialog(null, "Tem a certeza que pretende repor a sua palavra-passe?", "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, icon, respostas, respostas[0]);
                if (confirmDialog == 0) {
                    if (Arrays.equals(reporPassword.getPassword(), confirmarReporPassword.getPassword())) {
                        controller.setPASSWORD(String.valueOf(reporPassword.getPassword()));
                        final ImageIcon icon4 = new ImageIcon("src/main/resources/check.png");
                        controller.registarUtilizadorAlterado();
                        JOptionPane.showMessageDialog(null, "A sua palavra-passe foi alterada com sucesso.", "Informação", JOptionPane.ERROR_MESSAGE, icon4);
                        this.dispose();
                    }else{
                        final ImageIcon icon3 = new ImageIcon("src/main/resources/error.png");
                        JOptionPane.showMessageDialog(null, "As palavras-passe introduzidas não coincidem.", "Erro", JOptionPane.ERROR_MESSAGE, icon3);
                        reporPassword.setText("");
                        confirmarReporPassword.setText("");
                    }
                }
    }//GEN-LAST:event_botaoContinuarActionPerformed

