    private void continuarButtonActionPerformed(java.awt.event.ActionEvent evt) {
        String[] respostas = {"Sim", "Não"};
        final ImageIcon icon4 = new ImageIcon("src/main/resources/question.png");
        int confirmDialog = JOptionPane.showOptionDialog(null, "Tem a certeza que pretende guardar as alterações feitas na candidatura?", "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, icon4, respostas, respostas[0]);
        if (confirmDialog == 0) {
            String[] infoCand = new String[infoCandidatura.length];
            infoCand[0] = nomeEmpresaField.getText();
            infoCand[1] = moradaEmpresaField.getText();
            infoCand[2] = contactoField.getText();
            infoCand[3] = areaField.getText();
            infoCand[4] = numConvitesField.getText();
            infoCand[5] = caracterizacaoField.getText();
            boolean validacaoAlteracao = acctrl.alterarCandidaturaExposicao(infoCand);
            if (validacaoAlteracao) {
                acctrl.registaAlteracaoCandidaturaExposicao();
                final ImageIcon icon3 = new ImageIcon("src/main/resources/check.png");
                JOptionPane.showMessageDialog(null, "Alterações gravadas com sucesso.", "Erro", JOptionPane.ERROR_MESSAGE, icon3);
                this.dispose();
            } else {
                final ImageIcon icon2 = new ImageIcon("src/main/resources/error.png");
                JOptionPane.showMessageDialog(null, "Os dados que introduziu estão incorretos.", "Erro", JOptionPane.ERROR_MESSAGE, icon2);
            }

        }
    }

