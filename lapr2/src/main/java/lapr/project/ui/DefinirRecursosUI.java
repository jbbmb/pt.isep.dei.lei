    private void botaoCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCancelarActionPerformed
        String[] respostas = {"Sim", "Não"};
        final ImageIcon icon = new ImageIcon("src/main/resources/question.png");
        int confirmDialog = JOptionPane.showOptionDialog(null, "Tem a certeza que pretende cancelar a operação?", "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, icon, respostas, respostas[0]);
        if (confirmDialog == 0) {
            this.dispose();
        }
    }//GEN-LAST:event_botaoCancelarActionPerformed

    private void botaoContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoContinuarActionPerformed
        String[] respostas = {"Sim", "Não"};
        final ImageIcon icon2 = new ImageIcon("src/main/resources/question.png");
        boolean validacao = drctrl.setRecurso(nomeRecursoField.getText());
        if (validacao) {
            int confirmDialog = JOptionPane.showOptionDialog(null, "Pretende definir este recurso?", "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, icon2, respostas, respostas[0]);
            if (confirmDialog == 0) {
                drctrl.registarRecurso();
                listaRecursosModel.addElement(listaRecursos.get(listaRecursos.size()-1).getRecurso());
            }
            int repeatDialog = JOptionPane.showOptionDialog(null, "Pretende definir mais recursos?", "Confirmação", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, icon2, respostas, respostas[0]);
            if (repeatDialog == 0) {
                this.dispose();
                new DefinirRecursosUI(centroExposicoes).setVisible(true);
            } else {
                this.dispose();
            }
        }else{
            JOptionPane.showMessageDialog(null, "O recurso que inseriu é inválido.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
            
    }//GEN-LAST:event_botaoContinuarActionPerformed

