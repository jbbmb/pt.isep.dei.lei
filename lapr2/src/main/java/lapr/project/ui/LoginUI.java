    private void botãoEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botãoEntrarActionPerformed
        boolean loginValido = loginController.validaLogin(usernameField.getText(),
                String.valueOf(passwordField.getPassword()));
        if (loginValido) {
            this.dispose();
            java.awt.EventQueue.invokeLater(() -> {
                new MainUI(centro, usernameField.getText()).setVisible(true);
            });
        } else {
            final ImageIcon icon = new ImageIcon("src/main/resources/error.png");
            JOptionPane.showMessageDialog(null, "Credenciais de acesso inválidas!\nPor favor, confirme o seu nome de utilizador\ne a sua palavra-passe.", "Erro", JOptionPane.ERROR_MESSAGE, icon);
        }
    }//GEN-LAST:event_botãoEntrarActionPerformed

