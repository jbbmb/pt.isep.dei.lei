    private void adicionarRecursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarRecursoActionPerformed
        int indice = listaRecursosDisponiveis.getSelectedIndex();
        if (indice >= 0) {
            listaRecursosDemonstracaoModel.addElement(listaRecursosDisponiveis.getSelectedValue());
            controller.addRecurso(listaRecursosDisponiveis.getSelectedValue());
            listaRecursosDisponíveisModel.removeElement(listaRecursosDisponiveis.getSelectedValue());
        }
    }//GEN-LAST:event_adicionarRecursoActionPerformed

    private void retirarRecursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retirarRecursoActionPerformed
        if (listaRecursosPretendidos.getSelectedIndex() >= 0) {
            listaRecursosDisponíveisModel.addElement(listaRecursosPretendidos.getSelectedValue());
            controller.removerRecurso(listaRecursosPretendidos.getSelectedValue());
            listaRecursosDemonstracaoModel.removeElement(listaRecursosPretendidos.getSelectedValue());            
        }
    }//GEN-LAST:event_retirarRecursoActionPerformed

