    private void AdicionarOrganizadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AdicionarOrganizadorActionPerformed
        int indice = jList1.getSelectedIndex();
        if (indice >= 0) {
            listaOrganizadoresModel.addElement(jList1.getSelectedValue());
            controller.addOrganizador(controller.getListaUtilizadores()
                    .getUtilizador(jList1.getSelectedValue().toString()));
        }
    }//GEN-LAST:event_AdicionarOrganizadorActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String[] respostas = {"Sim", "Não"};
        final ImageIcon icon = new ImageIcon("src/main/resources/question.png");
        int confirmDialog = JOptionPane.showOptionDialog(null, "Tem a "
                + "certeza que pretende cancelar a operação?", "Atenção",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,
                icon, respostas, respostas[0]);
        if (confirmDialog == 0) {
            this.dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

