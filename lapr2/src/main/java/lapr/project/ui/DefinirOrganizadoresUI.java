    public DefinirOrganizadoresUI(CentroExposicoes centroExposicoes,
            CriarExposicaoController controller, String username) {
        this.centroExposicoes = centroExposicoes;
        this.controller = controller;
        ListaOrganizadores listaOrganizadores = controller.getListaOrganizadores();
        for (int i = 0; i < controller.getListaUtilizadores().
                getListaUtilizadores().size(); i++) {
            if (!username.equals(controller.getListaUtilizadores()
                    .getListaUtilizadores().get(i).getUsername())) {
                listaUtilizadoresModel.addElement(controller.getListaUtilizadores().
                        getListaUtilizadores().get(i).getUsername());
            }
        }
        for (int i = 0; i < listaOrganizadores.getListaOrganizadores().
                size(); i++) {
            listaOrganizadoresModel.addElement(listaOrganizadores.
                    getListaOrganizadores().get(i).getUtilizador().getUsername());
        }
        initComponents();
        setLocationRelativeTo(null);
    }

