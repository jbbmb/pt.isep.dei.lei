            int confirmDialog = JOptionPane.showOptionDialog(null, "Tem a certeza que pretende cancelar a operação?", "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, icon2, respostas, respostas[0]);
                if (confirmDialog == 0) {
                    this.dispose();
            }
